package io.github.dunwu.javacore.generics;

public class NotGenericsDemo {
    private Object x; // 表示X坐标
    private Object y; // 表示Y坐标

    public void setX(Object x) {
        this.x = x;
    }

    public void setY(Object y) {
        this.y = y;
    }

    public Object getX() {
        return this.x;
    }

    public Object getY() {
        return this.y;
    }
};
